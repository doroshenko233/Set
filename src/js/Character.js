class Character {
   constructor(name, type) {
     this.name = name;
     this.type = type;
   }
}
  
export const zombie = new Character('Nick', 'Zombie');
export const bowerman = new Character('Nick', 'Bowerman');
export const undead = new Character('Nick', 'Undead');
export const daemon = new Character('Nick', 'Daemon');

